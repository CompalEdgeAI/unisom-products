function getLangFromURL() {
  const u = new URL(window.location.href);
  const lang = u.searchParams.get("lang");
  return lang && I18N[lang] ? lang : null;
}

function setLangInURL(lang) {
  const u = new URL(window.location.href);
  u.searchParams.set("lang", lang);
  // Avoid jumping to top due to hash changes
  window.history.replaceState({}, "", u.toString());
}

function applyI18n(lang) {
  const dict = I18N[lang] || I18N.en;

  document.documentElement.lang = (lang === "zh-Hant") ? "zh-Hant"
    : (lang === "zh-Hans") ? "zh-Hans"
    : (lang === "ja") ? "ja"
    : (lang === "ko") ? "ko"
    : (lang === "de") ? "de"
    : (lang === "fr") ? "fr"
    : "en";

  // data-i18n => textContent
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const k = el.getAttribute("data-i18n");
    if (k && dict[k] != null) el.textContent = dict[k];
  });

  // data-i18n-html => innerHTML (limited use)
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const k = el.getAttribute("data-i18n-html");
    if (k && dict[k] != null) el.innerHTML = dict[k];
  });
}

function initI18n() {
  const urlLang = getLangFromURL();
  const saved = localStorage.getItem("lang");
  const lang = urlLang || (saved && I18N[saved] ? saved : "en");

  applyI18n(lang);
  setLangInURL(lang);

  const sel = document.getElementById("langSelect");
  if (sel) {
    sel.value = lang;
    sel.addEventListener("change", () => {
      const next = sel.value;
      localStorage.setItem("lang", next);
      applyI18n(next);
      setLangInURL(next);
    });
  }

  return lang;
}
